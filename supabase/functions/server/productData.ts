// Utility functions for server use
export const getFeaturedProducts = () => {
  return NEET_KAKA_PRODUCTS.filter(product => product.bestseller).slice(0, 6);
};

export const getProductById = (id: number) => {
  return NEET_KAKA_PRODUCTS.find(product => product.id === id);
};

export const searchProducts = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return NEET_KAKA_PRODUCTS.filter(product =>
    product.title.toLowerCase().includes(lowercaseQuery) ||
    product.author.toLowerCase().includes(lowercaseQuery) ||
    product.category.toLowerCase().includes(lowercaseQuery) ||
    product.subject.toLowerCase().includes(lowercaseQuery)
  );
};

export const getCategoryStats = () => {
  const categories = ["Featured Combos", "Individual Books", "UPSC", "NCERT Word-to-Word", "Charts", "Tools & Registers", "Motivational", "Tool Combos"];
  
  return categories.map(category => ({
    name: category,
    count: NEET_KAKA_PRODUCTS.filter(p => p.category === category).length,
    products: NEET_KAKA_PRODUCTS.filter(p => p.category === category).slice(0, 3)
  }));
};